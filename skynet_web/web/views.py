def getMetrics(tp, tn, fp, fn):
    if tp==0 and tn==0 and fp==0 and fn==0:
        accuracy=-1
    else:
        accuracy = (tp+tn)/(tp+tn+fp+fn)
    if tp==0 and fp==0:
        precision=-1
    else:
        precision = tp / (tp+fp)
    if tp==0 and fn==0:
        recall=-1
    else:
        recall = tp / (tp+fn)
    if recall==-1 or precision==-1:
        f1score=-1
    else:
        f1score = 2*recall*precision/(recall+precision)
    return [accuracy, precision, recall, f1score]

